//! Aliases types used to make it easier using long linear algebra types.

/// 2x2 floating matrix.
pub type M22 = ( (f32, f32), (f32, f32) );

/// 3x3 floating matrix.
pub type M33 = ( (f32, f32, f32), (f32, f32, f32), (f32, f32, f32) );

/// 4x4 floating matrix.
pub type M44 = ( (f32, f32, f32, f32), (f32, f32, f32, f32), (f32, f32, f32, f32), (f32, f32, f32, f32) );
